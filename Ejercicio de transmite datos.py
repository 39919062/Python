def transmite_megabites(m):
  return 100 * m

print(f"En 45 segundos la red transmite:         {transmite_megabites(45)} megabits por segundo")
print(f"En un minuto y medio la red transmite:   {transmite_megabites(90)} megabits por segundo")
print(f"En una hora la red transmite:            {transmite_megabites(3600)} megabits por segundo")
